    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function destroy(User $cajero)
    {

        $nombre = $cajero -> name;
        $cajero -> delete();

